
import { supabase } from '@/integrations/supabase/client';
import { generateProperties } from './utils/propertyGenerator';

export const seedDatabase = async () => {
  try {
    console.log('ğŸŒ± Starting database seeding with Supabase...');
    
    // Generate sample properties with more variety
    const sampleProperties = generateProperties(75);
    
    console.log(`ğŸ“ Generated ${sampleProperties.length} sample properties`);
    console.log('ğŸ  Property types:', [...new Set(sampleProperties.map(p => p.propertyType))]);
    console.log('ğŸ™ï¸ Locations:', [...new Set(sampleProperties.map(p => p.city))]);
    
    // Insert properties into Supabase in batches to avoid timeout
    const batchSize = 25;
    let insertedCount = 0;
    
    for (let i = 0; i < sampleProperties.length; i += batchSize) {
      const batch = sampleProperties.slice(i, i + batchSize);
      
      console.log(`ğŸ“¤ Inserting batch ${Math.floor(i / batchSize) + 1}/${Math.ceil(sampleProperties.length / batchSize)}...`);
      
      const { data, error } = await supabase
        .from('properties')
        .insert(batch.map(property => ({
          title: property.title,
          description: property.description,
          location: `${property.address}, ${property.city}, ${property.state}`,
          price: property.price,
          bedrooms: property.bedrooms,
          bathrooms: property.bathrooms,
          size_sqft: property.area,
          property_type: property.propertyType,
          status: property.status === 'active' ? 'available' : property.status === 'rented' ? 'rented' : 'available',
          images: property.images,
          amenities: property.amenities,
          agent_id: property.agentId,
          is_featured: property.isFeatured || false
        })))
        .select();

      if (error) {
        console.error(`âŒ Error inserting batch ${Math.floor(i / batchSize) + 1}:`, error);
        throw error;
      }

      insertedCount += data?.length || 0;
      console.log(`âœ… Batch ${Math.floor(i / batchSize) + 1} completed (${data?.length || 0} properties)`);
      
      // Small delay between batches to avoid overwhelming the database
      await new Promise(resolve => setTimeout(resolve, 100));
    }

    // Generate some sample applications for testing
    console.log('ğŸ‘¥ Creating sample applications...');
    
    // Get a few properties to create applications for
    const { data: properties, error: propError } = await supabase
      .from('properties')
      .select('id')
      .limit(10);
    
    if (!propError && properties && properties.length > 0) {
      const sampleApplications = properties.slice(0, 5).map(property => ({
        property_id: property.id,
        user_id: 'sample-user-' + Math.random().toString(36).substr(2, 9),
        status: ['pending', 'approved', 'rejected'][Math.floor(Math.random() * 3)],
        monthly_income: Math.floor(Math.random() * 500000) + 100000,
        employment_status: ['Employed', 'Self-employed', 'Student'][Math.floor(Math.random() * 3)],
        move_in_date: new Date(Date.now() + Math.random() * 60 * 24 * 60 * 60 * 1000).toISOString().split('T')[0],
        notes: 'Sample application generated by seed script'
      }));

      const { error: appError } = await supabase
        .from('applications')
        .insert(sampleApplications);

      if (appError) {
        console.warn('âš ï¸ Could not create sample applications:', appError.message);
      } else {
        console.log(`âœ… Created ${sampleApplications.length} sample applications`);
      }
    }

    console.log('\nğŸ‰ Database seeding completed successfully!');
    console.log(`ğŸ“Š Final Statistics:`);
    console.log(`   â€¢ Properties created: ${insertedCount}`);
    console.log(`   â€¢ Featured properties: ${sampleProperties.filter(p => p.isFeatured).length}`);
    console.log(`   â€¢ Locations: ${abujaDistricts.join(', ')}`);
    console.log(`   â€¢ Price range: â‚¦200,000 - â‚¦5,000,000`);
    console.log(`   â€¢ Property types: Apartments, Houses, Studios, Shared, Shortlets`);
    console.log('ğŸ’° All prices are in Nigerian Naira (â‚¦)');
    
  } catch (error) {
    console.error('ğŸ’¥ Error seeding database:', error);
    throw error;
  }
};

export const clearDatabase = async () => {
  try {
    console.log('ğŸ§¹ Clearing Supabase database...');
    
    // Define table names as literals to satisfy TypeScript
    const tableNames = ['payments', 'featured_requests', 'applications', 'properties'] as const;
    
    for (const tableName of tableNames) {
      console.log(`ğŸ—‘ï¸ Clearing ${tableName} table...`);
      const { error } = await supabase.from(tableName).delete().neq('id', '00000000-0000-0000-0000-000000000000');
      
      if (error) {
        console.error(`âŒ Error clearing ${tableName}:`, error);
      } else {
        console.log(`âœ… Cleared ${tableName} table`);
      }
    }
    
    console.log('ğŸ¯ Supabase database cleared successfully!');
  } catch (error) {
    console.error('ğŸ’¥ Error clearing Supabase database:', error);
    throw error;
  }
};

